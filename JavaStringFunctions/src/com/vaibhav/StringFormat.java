package com.vaibhav;

public class StringFormat {
	
	static	String str1 = String.format("%c", 'c');  
	
	static 	String str2 = String.format("%d", 10);
   
	static   String str3 = String.format("%s", "Virat Kohli");  
    
	static   String str4 = String.format("%f", 10.00);    
    
	static   String str5 = String.format("%x", 80);    //hexadecimal value  
    
	

	public static void main(String[] args) {
		
		 
	        
	        System.out.println(str1);  
	        System.out.println(str2);  
	        System.out.println(str3);  
	        System.out.println(str4);  
	        System.out.println(str5);  
	        
	}

}
